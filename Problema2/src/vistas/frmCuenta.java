    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        if (txtDeposito.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "EL Monto no puede estar vacio");
            txtDeposito.requestFocus();
        } else {
            try {
                obj_cuenta.depositar(Double.parseDouble(txtDeposito.getText()));
                txtDeposito.setText("");
                JOptionPane.showMessageDialog(rootPane, "El deposito se realizo con éxito");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error de Calculo de area");
            }

        }

    }//GEN-LAST:event_btnDepositarActionPerformed

