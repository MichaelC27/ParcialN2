    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombre.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "EL Nombre no puede estar vacio");
            txtNombre.requestFocus();
        } else if (txtNumero.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "El número de cuenta no puede estar vacio");
            txtNumero.requestFocus();
        } else if (txtSaldo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "El saldo no puede estar vacio");
            txtSaldo.requestFocus();
        } else {
            try {
                obj_cuenta = new Cuenta(Integer.parseInt(txtNumero.getText()), txtNombre.getText(), Double.parseDouble(txtSaldo.getText()));
                btnMostrar.setEnabled(true);
                btnDeposito.setEnabled(true);
                btnRetiro.setEnabled(true);
                Cuenta = Integer.parseInt(txtNumero.getText());
                Cliente = txtNombre.getText();
                saldo = Double.parseDouble(txtSaldo.getText());
                JTextField[] textFields = {txtNumero, txtNombre, txtSaldo};
                for (JTextField textField : textFields) {
                    textField.setText("");
                }
                JOptionPane.showMessageDialog(rootPane, "Los Datos se guardaron con éxito");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error de Calculo de area");
            }

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

